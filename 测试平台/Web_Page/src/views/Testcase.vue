<template>
    <v-data-table
            v-model="selected"
            :headers="headers"
            :items="desserts"
            sort-by="id"
            class="elevation-1"
            show-select
            item-key="id"
    >
        <template v-slot:top>
            <v-toolbar flat>
                <v-toolbar-title>测试用例</v-toolbar-title>
                <v-divider class="mx-4" inset vertical></v-divider>
                <v-spacer></v-spacer>
                <v-btn
                        color="success"
                        dark
                        class="mb-2"
                        @click="executeCase()"
                >执行用例
                </v-btn>
                <v-dialog
                        v-model="dialog"
                        max-width="500px"
                >

                    <template v-slot:activator="{ on, attrs }">
                        <v-btn
                                color="primary"
                                dark
                                class="mb-2"
                                v-bind="attrs"
                                v-on="on"
                        >新建用例
                        </v-btn>
                    </template>
                    <v-card>
                        <v-card-title>
                            <span class="text-h5">{{ formTitle }}</span>
                        </v-card-title>
                        <v-card-text>
                            <v-container>
                                <v-row>
                                    <v-col
                                            cols="12"
                                            sm="6"
                                            md="4"
                                    >
                                        <v-text-field
                                                v-model="editedItem.id"
                                                label="用例ID"
                                        ></v-text-field>
                                    </v-col>
                                    <v-col
                                            cols="12"
                                            sm="6"
                                            md="4"
                                    >
                                        <v-text-field
                                                v-model="editedItem.nodeid"
                                                label="nodeID"
                                        ></v-text-field>
                                    </v-col>
                                    <v-col
                                            cols="12"
                                            sm="6"
                                            md="4"
                                    >
                                        <v-text-field
                                                v-model="editedItem.remark"
                                                label="备注"
                                        ></v-text-field>
                                    </v-col>
                                </v-row>
                            </v-container>
                        </v-card-text>

                        <v-card-actions>
                            <v-spacer></v-spacer>
                            <v-btn
                                    color="blue darken-1"
                                    text
                                    @click="close"
                            >
                                取消
                            </v-btn>
                            <v-btn
                                    color="blue darken-1"
                                    text
                                    @click="save"
                            >
                                保存
                            </v-btn>
                        </v-card-actions>
                    </v-card>
                </v-dialog>
                <v-dialog v-model="dialogDelete" max-width="500px">
                    <v-card>
                        <v-card-title class="text-h5">确认删除该用例？</v-card-title>
                        <v-card-actions>
                            <v-spacer></v-spacer>
                            <v-btn color="blue darken-1" text @click="closeDelete">取消</v-btn>
                            <v-btn color="blue darken-1" text @click="deleteItemConfirm">确认</v-btn>
                            <v-spacer></v-spacer>
                        </v-card-actions>
                    </v-card>
                </v-dialog>
            </v-toolbar>
        </template>
        <template v-slot:item.actions="{ item }">
            <v-icon small class="mr-2" @click="editItem(item)">mdi-pencil</v-icon>
            <v-icon small @click="deleteItem(item)">mdi-delete</v-icon>
        </template>
        <!--    <template v-slot:no-data>-->
        <!--      <v-btn-->
        <!--              color="primary"-->
        <!--              @click="initialize"-->
        <!--      >-->
        <!--        Reset-->
        <!--      </v-btn>-->
        <!--    </template>-->
    </v-data-table>
</template>
<script>
    export default {
        data: () => ({
            selected: [],
            dialog: false,
            dialogDelete: false,
            headers: [
                {text: '用例ID', align: 'start', sortable: false, value: 'id',},
                {text: 'nodeID', value: 'nodeid'},
                {text: '用例备注', value: 'remark'},
                {text: 'Actions', value: 'actions', sortable: false},
            ],
            desserts: [],
            editedIndex: -1,
            editedItem: {
                id: '',
                nodeid: 0,
                remark: 0,
            },
            defaultItem: {
                name: '',
                calories: 0,
                fat: 0,
                carbs: 0,
                protein: 0,
            },
        }),

        computed: {
            formTitle() {
                return this.editedIndex === -1 ? '新增用例' : '编辑用例'
            },
        },

        watch: {
            dialog(val) {
                val || this.close()
            },
            dialogDelete(val) {
                val || this.closeDelete()
            },
        },

        created() {
            this.initialize()
        },

        methods: {
            initialize() {
                this.$api.testcase.getTestcase().then((result) => {
                    console.log(result)
                    this.desserts = result['data']['msg']['data']
                })
            },

            editItem(item) {
                this.editedIndex = this.desserts.indexOf(item)
                this.editedItem = Object.assign({}, item)
                this.dialog = true
            },

            deleteItem(item) {
                this.editedIndex = this.desserts.indexOf(item)
                this.editedItem = Object.assign({}, item)
                this.dialogDelete = true
            },

            deleteItemConfirm() {
                let delID = this.editedItem['id']
                this.$api.testcase.delTestcase(delID)
                this.desserts.splice(this.editedIndex, 1)
                this.closeDelete()
            },

            close() {
                this.dialog = false
                this.$nextTick(() => {
                    this.editedItem = Object.assign({}, this.defaultItem)
                    this.editedIndex = -1
                })
            },

            closeDelete() {
                this.dialogDelete = false
                this.$nextTick(() => {
                    this.editedItem = Object.assign({}, this.defaultItem)
                    this.editedIndex = -1
                })
            },

            save() {
                if (this.editedIndex > -1) {
                    //编辑
                    Object.assign(this.desserts[this.editedIndex], this.editedItem)
                    this.$api.testcase.editTestcase(this.editedItem)
                } else {
                    //新增
                    this.desserts.push(this.editedItem)
                    this.$api.testcase.createTestcase(this.editedItem)
                }
                this.close()
            },
            executeCase() {
                //执行测试用例
                console.log(this.selected)
                this.$api.task.addTask(this.selected).then((result) => {
                    console.log(result)
                    if (result.data.error === 0) {
                        alert("用例执行成功，请到测试任务页面查看")
                    } else {
                        alert("用例未执行成功，请检查日志信息")
                    }
                })
            },
        },
    }
</script>